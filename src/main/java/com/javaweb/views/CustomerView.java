package com.javaweb.views;

/**
 * 用户管理的视图
 * 
 * @author PengChan
 *
 */
public class CustomerView {
	private String customerid;
	private String customername;
	private String contactinfo;
	private String contactadd;
	private String vehicleid;
	private String platenum;
	private String carmodel;
	private String inspectiondate;
	private String milage;
	private String vehflag;

	public String getCustomerid() {
		return customerid;
	}

	public void setCustomerid(String customerid) {
		this.customerid = customerid;
	}

	public String getCustomername() {
		return customername;
	}

	public void setCustomername(String customername) {
		this.customername = customername;
	}

	public String getContactinfo() {
		return contactinfo;
	}

	public void setContactinfo(String contactinfo) {
		this.contactinfo = contactinfo;
	}

	public String getContactadd() {
		return contactadd;
	}

	public void setContactadd(String contactadd) {
		this.contactadd = contactadd;
	}

	public String getVehicleid() {
		return vehicleid;
	}

	public void setVehicleid(String vehicleid) {
		this.vehicleid = vehicleid;
	}

	public String getPlatenum() {
		return platenum;
	}

	public void setPlatenum(String platenum) {
		this.platenum = platenum;
	}

	public String getCarmodel() {
		return carmodel;
	}

	public void setCarmodel(String carmodel) {
		this.carmodel = carmodel;
	}

	public String getInspectiondate() {
		return inspectiondate;
	}

	public void setInspectiondate(String inspectiondate) {
		this.inspectiondate = inspectiondate;
	}

	public String getMilage() {
		return milage;
	}

	public void setMilage(String milage) {
		this.milage = milage;
	}

	public String getVehflag() {
		return vehflag;
	}

	public void setVehflag(String vehflag) {
		this.vehflag = vehflag;
	}

}
