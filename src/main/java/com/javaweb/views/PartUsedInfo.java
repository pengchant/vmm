package com.javaweb.views;

/**
 * 已经使用过的part
 * 
 * @author cp
 *
 */
public class PartUsedInfo {
	private String partusedid;

	private String partid;

	private String ordersid;

	private String partnumbering;

	private String partname;

	private String specifications;

	private String registedspecnum;

	private String receivednum;

	private String applicattime;

	private String userinfoid;

	public String getPartusedid() {
		return partusedid;
	}

	public void setPartusedid(String partusedid) {
		this.partusedid = partusedid;
	}

	public String getPartid() {
		return partid;
	}

	public void setPartid(String partid) {
		this.partid = partid;
	}

	public String getOrdersid() {
		return ordersid;
	}

	public void setOrdersid(String ordersid) {
		this.ordersid = ordersid;
	}

	public String getPartnumbering() {
		return partnumbering;
	}

	public void setPartnumbering(String partnumbering) {
		this.partnumbering = partnumbering;
	}

	public String getPartname() {
		return partname;
	}

	public void setPartname(String partname) {
		this.partname = partname;
	}

	public String getSpecifications() {
		return specifications;
	}

	public void setSpecifications(String specifications) {
		this.specifications = specifications;
	}

	public String getRegistedspecnum() {
		return registedspecnum;
	}

	public void setRegistedspecnum(String registedspecnum) {
		this.registedspecnum = registedspecnum;
	}

	public String getReceivednum() {
		return receivednum;
	}

	public void setReceivednum(String receivednum) {
		this.receivednum = receivednum;
	}

	public String getApplicattime() {
		return applicattime;
	}

	public void setApplicattime(String applicattime) {
		this.applicattime = applicattime;
	}

	public String getUserinfoid() {
		return userinfoid;
	}

	public void setUserinfoid(String userinfoid) {
		this.userinfoid = userinfoid;
	}

}
